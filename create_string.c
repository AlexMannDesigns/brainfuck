#include "brainfuck.h"

char	*create_string(void)
{
	char	*res;
	int		i;

	res = (char *) malloc(sizeof(char) * 2048);
	i = 0;
	while (i < 2048)
	{
		res[i] = '\0';
		i++;
	}
	return (res);
}
